"""Создание тестового PDF для проверки EMIAS Check."""

import fitz

FONT_PATH = "/System/Library/Fonts/Supplemental/Arial.ttf"

doc = fitz.open()

def write_page(doc, text):
    page = doc.new_page(width=595, height=842)
    rect = fitz.Rect(50, 50, 545, 792)
    # Insert as text block with embedded font
    tw = fitz.TextWriter(page.rect)
    font = fitz.Font(fontfile=FONT_PATH)
    y = 50
    for line in text.strip().split("\n"):
        if y > 780:
            page = doc.new_page(width=595, height=842)
            tw = fitz.TextWriter(page.rect)
            y = 50
        tw.append((50, y), line, font=font, fontsize=10)
        y += 14
    tw.write_text(page)
    return page

