@lru_cache(maxsize=256)
def _compiled_pattern(concept_name: str) -> re.Pattern[str] | None:
    """Скомпилировать regex-паттерн для концепта (с кэшем)."""
    terms = CONCEPTS.get(concept_name)
    if not terms:
        return None
    joined = "|".join(f"(?:{t})" for t in terms)
    return re.compile(joined, re.IGNORECASE)


def has_concept(concept_name: str, text: str) -> bool:
    """Проверить, содержит ли текст упоминание концепта.

    Args:
        concept_name: имя концепта из словаря CONCEPTS.
        text: текст для поиска.

    Returns:
        True если хотя бы один синоним найден.

    Raises:
        KeyError: концепт не найден в словаре.
    """
    pattern = _compiled_pattern(concept_name)
    if pattern is None:
        raise KeyError(f"Концепт не найден: {concept_name!r}")
    return bool(pattern.search(text))


def find_concept(concept_name: str, text: str) -> str | None:
    """Найти первое вхождение концепта и вернуть совпавший фрагмент.

    Returns:
        Совпавшая подстрока или None.
    """
    pattern = _compiled_pattern(concept_name)
    if pattern is None:
        raise KeyError(f"Концепт не найден: {concept_name!r}")
    match = pattern.search(text)
    return match.group() if match else None


def list_concepts() -> list[str]:
    """Вернуть список всех доступных концептов."""
    return sorted(CONCEPTS.keys())
